function initialize_chords(){
  $('.chord').tooltip({
    template: tooltip_template,
    toggle: 'tooltip',
    title: function(tip, element) {
        var chord_name = $(this).data('chord')
        return chords[chord_name];
    },
  })
};

var tooltip_template = '<div class="tooltip" role="tooltip"><div class="tooltip-arrow"></div><pre class="tooltip-inner"></pre></div>';

var chords = new Array();

function get_string(position){
  switch (position) {
    case '0': return "┬───┬───┬───┬──\n";
    case '1': return "┬─•─┬───┬───┬──\n";
    case '2': return "┬───┬─•─┬───┬──\n";
    case '3': return "┬───┬───┬─•─┬──\n";
    case '4': return "┬───┬───┬───┬─•\n";
    case 'x': return "\n";
  }
}

function start_of_string(guitar_string){
  switch (guitar_string) {
    case 1: return "E ";
    case 2: return "B ";
    case 3: return "G ";
    case 4: return "D ";
    case 5: return "A ";
    case 6: return "E ";
  }
}

function get_silents(note){
  if (note == 'x'){
    return(' x ');
  } else {
    return(' • ');
  }
}

